import Foundation

// MARK: - 日期名称格式枚举
public enum SBDayNameStyle {
    /// 日期名称的 3 个字母日期缩写
    case threeLetters
    /// 日期名称的 1 个字母日期缩写
    case oneLetter
    /// 完整的天名称
    case full
}

// MARK: - 月份名称格式枚举
public enum SBMonthNameStyle {
    /// 3 个字母月份的月份名称缩写
    case threeLetters
    /// 月份名称的 1 个字母月份缩写
    case oneLetter
    /// 完整的月份名称
    case full
}
